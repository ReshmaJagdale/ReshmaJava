package Variable;

public class Stringcantenationexample {
	
	public static void main (String[] args) {
		//Variable Declaration
		//syntax:  DataType VariableName;
		
		String InstituteName;
		String Coursename;
		String FirstName;
		String LastName;
		String MailID;
		int PassoutYear;
		int RollNumber;
		char Grade;
		float Percentage;
		
		//Variable Initioalisation
		//syntax: VariableName= VariableValue;
		InstituteName="Velocity Training center";
		Coursename= "Software Testing Course";
		FirstName="Reshma";
		LastName="Jagdale";
		MailID="jadale.reshma04@gmail.com";
		PassoutYear= 2012;
		RollNumber= 45;
		Grade='A';
		Percentage=70.03f;
		
		//usage
		System.out.println("InstituteName====>"  +InstituteName);
		System.out.println("Coursename====>"  +Coursename);
		System.out.println("FirstName====>"  +FirstName);
		System.out.println("LastName====>"  +LastName);
		System.out.println("MailID====>"  +MailID);
		System.out.println("PassoutYear====>"  +PassoutYear);
		System.out.println("RollNumber====>"  +RollNumber);
		System.out.println("Grade====>"  +Grade);
		System.out.println("Percentage====>"  +Percentage);
							
		
	}

}
