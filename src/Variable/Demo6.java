package Variable;

public class Demo6 {

	public static void main(String [] args) {
		//variable Declaration
		//Syntax--->DataType VariableName;
		
        String FirstName;
        String MiddleName;
        String SurName;
        int RollNumber;
        String CompanyName;
        int ExperienceYear;
        int YearOfPassing;
        float Percentage;
        char Grade;
        
        //Variable Initialisation
        //Syntax---> VariableName VariableValue;
        
        FirstName="Tanvi";
        MiddleName="Ashish";
        SurName="Jagdale";
        RollNumber=45;
        CompanyName="Tom Tom Pvt Ltd";
        ExperienceYear= 8;
        YearOfPassing=2012;
        Percentage=70.05f;
        Grade='A';
        
        
        //Usage
        //String Catenation
        
        System.out.println("FirstName==>"  +FirstName);
        System.out.println("MiddleName==>"  +MiddleName);
        System.out.println("SurName==>"  +SurName);
        System.out.println("RollNumber==>"  +RollNumber);
        System.out.println("CompanyName==>"  +CompanyName);
        System.out.println("ExperienceYear==>"  +ExperienceYear);
        System.out.println("YearOfPassing==>"  +YearOfPassing);
        System.out.println("Percentage==>"  +Percentage);
        System.out.println("Grade==>"  +Grade);
        
          
	}
}
