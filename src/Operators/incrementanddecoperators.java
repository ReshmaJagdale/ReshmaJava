package Operators;

public class incrementanddecoperators {

	public static void main(String[] args) {
		int a=20;
		int b=40;
		int c;
		c=a;
		System.out.println(c);
		System.out.println(a);
		
		//ncrement by 1
		a++;   //21
		System.out.println(a);
		
		a=a+1;    //22
		System.out.println(a);
		
		//increment by 2
		a=a+2; //24
		System.out.println(a);
		
		//Increment by 5
		a=a+5;  //29
		System.out.println(a);
		
		a+=1;
		System.out.println(a);
		
		//Decrement operator
		System.out.println(b);
		
		b--;   // dec by 1
		System.out.println(b);
		
		b=b-1; //dec by 1
		System.out.println(b);
		
		b-=1;
		System.out.println(b);
		
		
	}

}
