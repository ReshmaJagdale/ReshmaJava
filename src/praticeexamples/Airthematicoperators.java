package praticeexamples;

public class Airthematicoperators {

	public static void main(String[] args) {
	//Airthematic operators
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println(c);
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a%b);
		a++;
		System.out.println("Value of a===>" +a);
		
		a--;
		System.out.println("Value of a===>" +a);
		
		a=a+1;
		System.out.println("Value of a===>" +a);
	}

}
