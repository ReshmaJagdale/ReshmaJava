package DataType;

public class shortexample {
	
	public static void main( String [] args)
	{
		//DataType VariableName= Vairiable Value;
		//Primitive Datatype
		//Example of Short    Range of short(-32768 to 32767)
		
		short S1=20;
		System.out.println("S1---->" +S1);
		
		short S2=-32768;  //Minimum Value
		System.out.println("S2---->"  +S2);
		
		short S3=32767;  //Maximum Value 32767
		System.out.println("S3---->"  +S3);
		
		short S4=-32;  //  any value between (-32768 to 32767)
		System.out.println("S4---->"  +S4);		
		
		short S5=-32766;  //  any value between (-32768 to 32767)
		System.out.println("S5---->"  +S5);
	}
}
