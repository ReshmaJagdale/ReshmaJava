package ControlSatement;

public class IfElseStatement2 {

	public static void main(String[] args) {
		//Check whether number is odd or even
		//Syntax: Datatype Variablename = Variablevalue;
		int number = 15;
		
		if (number % 2==0) {
			System.out.println(number + "The number is even");
		}
		else {
			System.out.println(number + "The number is odd");
		}

	}

}
